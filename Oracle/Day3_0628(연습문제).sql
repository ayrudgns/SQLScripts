CREATE TABLE SCORES (
	STUDENT_NUM CHAR(7) NOT NULL,
	SCORE_SUB VARCHAR2(6) NOT NULL,
	SCORE NUMBER(2) NOT NULL,
	TEACHER NVARCHAR2(3) NOT NULL,
	TERM CHAR(6) NOT NULL
	PRIMARY KEY (STUDENT_NUM, SCORE_SUB),		-- 기본 키 그룹화	
	FOREIGN KEY (STUDENT_NUM) REFERENCES STUDENTS(STUDENT_NUM)		-- 참조키 (참조시킬 컬럼) REFERENCES 부모테이블명 (부모컬럼)
);

